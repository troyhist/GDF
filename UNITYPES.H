#ifndef _UNITYPES
#define _UNITYPES
// -----------------------------------------------------------------------
// Bits in byte
// -----------------------------------------------------------------------
#define _UTBIB  (8)             // Default for PC
#define WORDSIZE 	(16)	// Size of platform's word in bytes
// -----------------------------------------------------------------------
// int64
// -----------------------------------------------------------------------
#if (sizeof(int)*_UTBIB==64)
#define int64   int
#undef	WORDSIZE
#define WORDSIZE 	(64)
#else
#define int64   long
#endif
// -----------------------------------------------------------------------
// int32
// -----------------------------------------------------------------------
#if (sizeof(int)*_UTBIB==32)
#define int32   int
#undef	WORDSIZE
#define WORDSIZE	(32)
#else
#define int32   long
#endif
// -----------------------------------------------------------------------
// int16
// -----------------------------------------------------------------------
#if (sizeof(int)*_UTBIB==16)
#define int16   int
#undef	WORDSIZE
#define WORDSIZE	(16)
#else
#define int16   short
#endif
// -----------------------------------------------------------------------
// int8
// -----------------------------------------------------------------------
#if (sizeof(int)*_UTBIB==8)
#define int8    int
#undef	WORDSIZE
#define WORDSIZE	(8)
#else
#define int8    char
#endif
// -----------------------------------------------------------------------
#endif
